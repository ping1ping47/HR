  methods: {
    showFormattedDate() {
      const dateElement = this.$refs.dateDisplay;
      const currentDate = new Date();
      const options = {
        weekday: "long",
        year: "numeric",
        month: "long",
        day: "numeric",
      };
      const formattedDate = new Intl.DateTimeFormat("th-TH", options).format(
        currentDate
      );
      dateElement.innerText = formattedDate;
    },
  },
};
